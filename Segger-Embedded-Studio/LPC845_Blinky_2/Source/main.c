#include <stdio.h>
#include <stdlib.h>
//#include "clock_config.h"
//#include "pin_mux.h"
#include "fsl_gpio.h"

/*********************************************************************
*
*       main()
*
*  Function description
*   Application entry point.
*/
void main(void) {
    long i;

  /* Define the init structure for the output LED pin*/
    gpio_pin_config_t led_config = {
        kGPIO_DigitalOutput,
        0,
    };

    //BOARD_InitBootClocks();
    //BOARD_InitBootPins();

    /* Init output LED GPIO. */
    GPIO_PortInit(GPIO, 1);
    GPIO_PinInit(GPIO, 1, 0, &led_config);

    while (1)
    {
        for (i = 32000; i > 0; i--);
        GPIO_PortToggle(GPIO, 1, 1u << 0);
    }
}

